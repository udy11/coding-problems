    for i in ii.split(','):
        fish[int(i)] += 1
    for d in range(1, days + 1):
        f1 = [0 for k in range(9)]
        for k in range(1, 9):
            f1[k-1] = fish[k]
        f1[6] += fish[0]
        f1[8] = fish[0]
        fish = list(f1)
    return sum(fish)

